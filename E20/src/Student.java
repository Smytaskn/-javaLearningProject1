public class Student implements Comparable{
    int number;
    int id;
    public number(int number){
        this.number=number;
    }
    public id(int id){
        this.id=id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public void getNumber(){
        return number;
    }
}
